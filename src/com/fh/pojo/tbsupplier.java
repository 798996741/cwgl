package com.fh.pojo;

public class tbsupplier {
    private Long supid;
    private String csname;
    private String supfname;
    private String supsname;
    private String supno;

    @Override
    public String toString() {
        return "tbsupplier{" +
                "supid=" + supid +
                ", csname='" + csname + '\'' +
                ", supfname='" + supfname + '\'' +
                ", supsname='" + supsname + '\'' +
                ", supno='" + supno + '\'' +
                '}';
    }

    public Long getSupid() {
        return supid;
    }

    public void setSupid(Long supid) {
        this.supid = supid;
    }

    public String getCsname() {
        return csname;
    }

    public void setCsname(String csname) {
        this.csname = csname;
    }

    public String getSupfname() {
        return supfname;
    }

    public void setSupfname(String supfname) {
        this.supfname = supfname;
    }

    public String getSupsname() {
        return supsname;
    }

    public void setSupsname(String supsname) {
        this.supsname = supsname;
    }

    public String getSupno() {
        return supno;
    }

    public void setSupno(String supno) {
        this.supno = supno;
    }
}
